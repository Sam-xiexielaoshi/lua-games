function love.conf(t)
    t.window.title = 'Save the Ball'
    t.window.width = 800
    t.window.height = 600
    t.window.resizable = true
    t.window.minwidth = 400
    t.window.minheight = 300
    t.window.fullscreen = false
    t.window.vsync = 1
    t.window.msaa = 4
    t.window.display = 2
end